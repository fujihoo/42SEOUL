int	ft_str_is_printable(char *str)
{
	int	check;
	int	i;

	check = 1;
	i = 0;
	while (str[i])
	{
		if (str[i] <= 31 || str[i] >= 127)
			check = 0;
		i++;
	}
	return (check);
}
