int	ft_str_is_uppercase(char *str)
{
	int	check;
	int	i;

	check = 1;
	i = 0;
	while (str[i])
	{
		if (str[i] < 'A' || str[i] > 'Z')
			check = 0;
		i++;
	}
	return (check);
}
